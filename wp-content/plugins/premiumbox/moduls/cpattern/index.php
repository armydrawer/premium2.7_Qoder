function cpattern_check($account, $cpattern) {
	
	if ('bitcoin' == $cpattern) {
		$cpattern = 'crypto_BTC';
	}
	if ('luna' == $cpattern) {
		if (!is_valid_credit_card($account)) {
			return 0;
		}	
	} elseif (strstr($cpattern, 'crypto_')) {
		
		$regex = get_cpattern();
		$cpattern = str_replace('crypto_', '', $cpattern);
		$reg = trim(is_isset($regex, $cpattern));
		if ($reg) {
			if (!preg_match("/{$reg}/", $account, $matches)) {
				return 0;	
			}
		}
		
	}
	
	return apply_filters('cpattern_check', 1, $account, $cpattern);
}

add_filter('error_bids', 'cpattern_error_bids', 40, 4);  
function cpattern_error_bids($error_bids, $direction, $vd1, $vd2) {
	
	if ($vd1->cpattern and isset($error_bids['bid']['account_give']) and $error_bids['bid']['account_give']) {
